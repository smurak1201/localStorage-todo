/**
 * Todo型定義
 *
 * 【各プロパティの説明】
 * - id: 各Todoを一意に識別するための文字列
 *   　　 並び替え時にReactが正確に要素を追跡するために必要
 * - text: Todoの内容（ユーザーが入力したテキスト）
 *
 * 【使用例】
 * const todo: Todo = {
 *   id: "todo-1234567890-abc123",
 *   text: "買い物に行く"
 * };
 */
export type Todo = {
  id: string; // 一意のID（例: "todo-1234567890-abc123"）
  text: string; // Todoの内容（例: "買い物に行く"）
};
